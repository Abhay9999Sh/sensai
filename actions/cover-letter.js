"use server";

import { db } from "@/lib/prisma";
import { auth } from "@clerk/nextjs/server";
import { GoogleGenerativeAI } from "@google/generative-ai";
import { revalidatePath } from "next/cache";

//step -1 = gemini setup
const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({
  model: "gemini-2.5-flash",
});

export async function saveCoverLetter(content) {
  //step-2
  //a two-step security process: Step 1: "Are you logged in?" (Clerk check)
  //Authentication = "Who are you?"
  const { userId } = await auth();
  if (!userId) throw new Error("Unauthorized");

  // Step 2: "Do we have your information?" (Database check)
  // Authorization = "What are you allowed to do?"
  const user = await db.user.findUnique({
    where: {
      clerkUserId: userId,
    },
  });

  if (!user) throw new Error("User not found");

  //
  try {
    const coverLetter = await db.coverLetter.upsert({
      //update if present or insert if not present

      where: {
        userId: user.id,
      },
      update: {
        content,
        jobDescription,
        companyName,
        jobTitle,
      },
      create: {
        userId: user.id,
        content,
        jobDescription,
        companyName,
        jobTitle,
      },
    });

    console.log("coverLetter = ", coverLetter);
    revalidatePath("/ai-cover-letter");
  } catch (error) {
    console.log("Error saving cover-letter", error.message);
    throw new Error("Failed to save cover-letter");
  }
}

//get all cover letters

export async function getCoverLetters() {
  const { userId } = await auth();
  if (!userId) throw new Error("Unauthorized");

  const user = await db.user.findUnique({
    where: {
      clerkUserId: userId,
    },
  });

  if (!user) throw new Error("User not found");

  return await db.coverLetter.findMany({
    where: {
      userId: user.id,
    },
    orderBy: {
      createdAt: "desc", //most recent first
    },
  });
}

